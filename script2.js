let get = document.getElementById("get-rate");
get.addEventListener('click', function() {
  const amount = document.getElementById("amount").value;
  const fromCurrency = document.getElementById("from").value;
  const toCurrency = document.getElementById("to").value;
  const apiKey = "253e0f847ddd8846e912efdb";
  const apiUrl = `https://v6.exchangerate-api.com/v6/${apiKey}/pair/${fromCurrency}/${toCurrency}`;

  fetch(apiUrl)
      .then(response => response.json()) 
      .then(data => {
          if (data.result === "success") {
              const exchangeRate = data.conversion_rate;
              const convertedAmount = (amount * exchangeRate).toFixed(2);
              document.getElementById('exchange-rate').innerHTML = `<p>${amount} ${fromCurrency} = ${convertedAmount} ${toCurrency}</p>`;
          } else {
              console.error('Error: API request failed');
              document.getElementById('exchange-rate').innerHTML = `<p>Error: Unable to fetch exchange rate</p>`;
          }
      })
      .catch(error => {
          console.error('Error fetching data:', error);
          document.getElementById('exchange-rate').innerHTML = `<p>Error: Unable to fetch exchange rate</p>`;
      });
});
